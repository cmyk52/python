# Podemos hacer comparaciones logicas donde nos devolvera una booleano

numeroUno = 10
numeroDos = 15

print(numeroUno > numeroDos)  # Mayor que
print(numeroUno < numeroDos)  # Menor que
print(numeroUno <= 10)  # Menor o igual que
print(numeroUno >= 10)  # Mayor o igual que
print(numeroDos == 20)  # Igualdad que
print(numeroDos != 2)  # Es distinto que
